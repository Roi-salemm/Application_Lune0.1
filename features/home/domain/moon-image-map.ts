export function getMoonImageByAgeDays(
  ageDays: number | null | undefined
): ImageSourcePropType | null {
  if (ageDays === null || ageDays === undefined) {
    return null;
  }
  if (!Number.isFinite(ageDays)) {
    return null;
  }
  if (!LUNAISON_2H_IMAGES.length) {
    return null;
  }

  const clamped = Math.max(0, ageDays);
  const stepIndex = Math.floor((clamped * HOURS_PER_DAY) / LUNAISON_2H_STEP_HOURS);
  const index = Math.min(LUNAISON_2H_IMAGES.length - 1, Math.max(0, stepIndex));
  return LUNAISON_2H_IMAGES[index] ?? null;
}
